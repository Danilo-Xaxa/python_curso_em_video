x = input('Qual é a sua turma da faculdade? ')
print('Turma:', x)
print('Tipo primitivo:', type(x))
print('Contém somente números?', x.isnumeric())
print('Contém somente letras?', x.isalpha())
print('Contém somente letras e números?', x.isalnum())
print('Todas suas letras são maiúsculas?', x.isupper())
print('Todas suas letras são minúsculas?', x.islower())
